#include "linfunc.h"


void linfunc_init_points(linfunc_t *func, float x1, float y1, float x2, float y2)
{
   func->m = (y2 - y1) / (x2 - x1);
   func->n = y1 - func->m * x1;
}


float linfunc_calc(linfunc_t *func, float x)
{
   return func->m * x + func->n;   
}

