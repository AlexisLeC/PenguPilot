from scl import generate_map
from misc import daemonize


def main(name):
   map = generate_map(name)
   socket_in = map['log_data']
   socket_out = map['log_data_pub']
   while True:
      data = socket_in.recv()
      socket_out.send(data)

daemonize('log_proxy', main)

